package day05;
//유자 + 설탕
public class Yuja {
	private int yuja;
	private int sugar;
	
	//setter, getter
	public int getYuja() {
		return yuja;
	}
	public void setYuja(int yuja) {
		this.yuja = yuja;
	}
	public int getSugar() {
		return sugar;
	}
	public void setSugar(int sugar) {
		this.sugar = sugar;
	}
	
	
}
